var rnd *rand.Rand

func init() {
	// 初始化随机数生成器
	rnd = rand.New(rand.NewSource(time.Now().UnixNano()))
}

// GetRandomAvatar 返回一个随机选择的头像URL
func GetRandomAvatar() string {
	if len(avatarURLs) == 0 {
		return ""
	}
	return avatarURLs[rnd.Intn(len(avatarURLs))]
}
